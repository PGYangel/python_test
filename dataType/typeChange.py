'''
有时候，我们需要对数据内置的类型进行转换，数据类型的转换，你只需要将数据类型作为函数名即可。
以下几个内置的函数可以执行数据类型之间的转换。这些函数返回一个新的对象，表示转换的值。
'''
'''------------------------------------------------------------------------------------------------------------------'''
'''
将x转换为一个整数
int(x [,base])
class int(x, base=10)
x -- 字符串或数字。
base -- 进制数，默认十进制。
'''
int()  # 不传入参数时，得到结果0
# 0
int(3)
# 3
int(3.6)
# 3
int('12', 16)  # 如果是带参数base的话，12要以字符串的形式进行输入，12 为 16进制
# 18
int('0xa', 16)
# 10
int('10', 8)
# 8
'''------------------------------------------------------------------------------------------------------------------'''



